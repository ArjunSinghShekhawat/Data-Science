# Q3. How do you convert one data type to another in Python?
"""In python one data type convert to another data type by using type casting 
In python there are two types of type casting available 
1.implicit
2.explicit
"""
#explicit type casting
print(int(123.544))
print(str(1))

#implicit type casting
num = 10
val = 21.43
print(num+val)